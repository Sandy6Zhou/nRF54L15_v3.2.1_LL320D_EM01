#ifndef _TEMP_HUMI_API_H_
#define _TEMP_HUMI_API_H_

#include <stdint.h>
#include <stdbool.h>

/* 温湿度操作结果 */
typedef enum
{
    TEMP_HUMI_SUCCESS = 0,
    TEMP_HUMI_ERROR_INIT,          /* 初始化错误 */
    TEMP_HUMI_ERROR_COMM,          /* 通信错误 */
    TEMP_HUMI_ERROR_CRC,           /* CRC 校验错误 */
    TEMP_HUMI_ERROR_PARAM,         /* 参数错误 */
    TEMP_HUMI_ERROR_RANGE,         /* 数据超出有效范围 */
} temp_humi_result_t;

/* 温湿度数据结构体 */
typedef struct
{
    int16_t temperature;           /* 温度值，单位 0.01°C，如 2550 表示 25.50°C */
    uint16_t humidity;             /* 湿度值，单位 0.01%RH，如 6500 表示 65.00% */
}temp_humi_data_t;

/********************************************************************
**函数名称:  temp_humi_api_init
**入口参数:  无
**出口参数:  无
**函数功能:  初始化温湿度模块，包括底层驱动和电源使能
**返 回 值:  TEMP_HUMI_SUCCESS 表示成功，其他表示错误码
*********************************************************************/
temp_humi_result_t temp_humi_api_init(void);

/********************************************************************
**函数名称:  temp_humi_api_read
**入口参数:  data     ---        温湿度数据存储指针（输出）
**出口参数:  data     ---        包含温度和湿度的结构体
**函数功能:  执行一次温湿度测量（唤醒→测量→休眠）
**返 回 值:  TEMP_HUMI_SUCCESS 表示成功，其他表示错误码
*********************************************************************/
temp_humi_result_t temp_humi_api_read(temp_humi_data_t *data);

/********************************************************************
**函数名称:  temp_humi_api_get_id
**入口参数:  id       ---        ID 存储指针（输出）
**出口参数:  id       ---        传感器设备 ID
**函数功能:  读取温湿度传感器设备 ID
**返 回 值:  TEMP_HUMI_SUCCESS 表示成功，其他表示错误码
*********************************************************************/
temp_humi_result_t temp_humi_api_get_id(uint16_t *id);

/********************************************************************
**函数名称:  temp_humi_api_sleep
**入口参数:  无
**出口参数:  无
**函数功能:  使传感器进入低功耗休眠模式
**返 回 值:  TEMP_HUMI_SUCCESS 表示成功，其他表示错误码
*********************************************************************/
temp_humi_result_t temp_humi_api_sleep(void);

/********************************************************************
**函数名称:  temp_humi_api_wakeup
**入口参数:  无
**出口参数:  无
**函数功能:  唤醒传感器
**返 回 值:  TEMP_HUMI_SUCCESS 表示成功，其他表示错误码
*********************************************************************/
temp_humi_result_t temp_humi_api_wakeup(void);

#endif /* _TEMP_HUMI_API_H_ */
