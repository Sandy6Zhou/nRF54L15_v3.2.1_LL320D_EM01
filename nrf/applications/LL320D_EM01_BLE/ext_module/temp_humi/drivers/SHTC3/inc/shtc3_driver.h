#ifndef _SHTC3_DRIVER_H_
#define _SHTC3_DRIVER_H_

#include <stdint.h>
#include <stdbool.h>

/* SHTC3 I2C 设备地址（7-bit） */
#define SHTC3_I2C_ADDR          0x70

/* SHTC3 命令定义（16-bit，MSB 优先） */
#define SHTC3_CMD_WAKEUP        0x3517      /* 唤醒传感器 */
#define SHTC3_CMD_SLEEP         0xB098      /* 进入休眠模式 */
#define SHTC3_CMD_SOFT_RESET    0x805D      /* 软件复位 */
#define SHTC3_CMD_READ_ID       0xEFC8      /* 读取设备 ID */
#define SHTC3_CMD_MEASURE       0x6458      /* 低功耗模式测量，温度优先 */

/********************************************************************
**函数名称:  shtc3_driver_wakeup
**入口参数:  无
**出口参数:  无
**函数功能:  唤醒传感器
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int shtc3_driver_wakeup(void);

/********************************************************************
**函数名称:  shtc3_driver_sleep
**入口参数:  无
**出口参数:  无
**函数功能:  使传感器进入休眠模式
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int shtc3_driver_sleep(void);

/********************************************************************
**函数名称:  shtc3_driver_init
**入口参数:  无
**出口参数:  无
**函数功能:  初始化 SHTC3 驱动，包括 I2C 总线和电源控制
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int shtc3_driver_init(void);

/********************************************************************
**函数名称:  shtc3_driver_read_measurement
**入口参数:  temperature  ---        温度存储指针（输出）
**           humidity     ---        湿度存储指针（输出）
**出口参数:  temperature  ---        温度值，单位 0.01°C
**           humidity     ---        湿度值，单位 0.01%RH
**函数功能:  执行一次完整测量（唤醒→测量→休眠）
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int shtc3_driver_read_measurement(int16_t *temperature, uint16_t *humidity);

/********************************************************************
**函数名称:  shtc3_driver_read_id
**入口参数:  id       ---        ID 存储指针（输出）
**出口参数:  id       ---        传感器设备 ID
**函数功能:  读取 SHTC3 设备 ID
**返 回 值:  0 表示成功，负值表示失败
*********************************************************************/
int shtc3_driver_read_id(uint16_t *id);

#endif /* _SHTC3_DRIVER_H_ */
