#ifndef _MY_FLASH_STORE_H_
#define _MY_FLASH_STORE_H_

/* FLASH循环存储测试开关：置1开启shell测试命令及调试接口 */
#define FS_STORE_TEST_ENABLE        0

/* 数据类型选择：TAG扫描数据 / 透传MAC扫描数据 */
typedef enum
{
    FS_TYPE_TAG = 0,        // TAG扫描结果数据
    FS_TYPE_MAC,            // 透传MAC扫描结果数据
    FS_TYPE_MAX             // 类型数量
} fs_data_type_t;

#if FS_STORE_TEST_ENABLE
/* 调试状态信息：用于测试或日志查看模块内部环形指针等运行状态 */
typedef struct
{
    uint16_t wr_sector;             // 下一个要写入的扇区索引(区内)
    uint16_t rd_sector;             // 当前最旧、待上报的扇区索引(区内)
    uint16_t valid_sectors;         // 当前已写入FLASH的有效扇区数
    uint16_t rd_rec_idx;            // 最旧扇区里开头已确认发过的条数
    uint32_t seq_counter;           // 扇区写序号(预留+诊断用途)
    uint16_t staging_count;         // RAM暂存缓冲当前记录数
    uint16_t rec_per_sector;        // 每扇区记录槽数
    uint16_t region_sectors;        // 环形区扇区总数
    uint16_t upload_staging_snap;   // 本轮上报开始时记下的暂存记录数(本轮最多发这么多)
    uint32_t read_offset;           // 本轮已从FLASH读出的记录数(从rd起点算起)
    uint16_t staging_read_idx;      // 本轮已从暂存缓冲读出的记录数
    bool     upload_active;         // 上报进行中标志(为true时暂停落盘、不动读写指针)
    uint32_t pending_count;         // 待上报记录总数
} fs_debug_info_t;
#endif /* FS_STORE_TEST_ENABLE */

/********************************************************************
**函数名称:  my_flash_store_init
**入口参数:  无
**出口参数:  无
**函数功能:  初始化FLASH循环存储模块：打开ble_data_storage分区，从ZMS加载
**           TAG/MAC两区元数据并校验，重建读写指针；元数据缺失或校验失败时
**           按空环初始化
**返 回 值:  0表示成功，负值表示失败
**注意事项:  须在ZMS挂载完成(my_param_load_config)之后调用
*********************************************************************/
int my_flash_store_init(void);

/********************************************************************
**函数名称:  my_flash_store_push_tag
**入口参数:  rec_ptr   ---        待存储的TAG扫描结果指针（输入）
**出口参数:  无
**函数功能:  追加一条TAG记录到TAG区RAM暂存缓冲；暂存满一整扇区则批量写入
**           FLASH并前移写指针，FLASH满环时覆盖最旧扇区
**返 回 值:  0表示成功，负值表示失败
*********************************************************************/
int my_flash_store_push_tag(const tag_scan_result_t *rec_ptr);

/********************************************************************
**函数名称:  my_flash_store_push_mac
**入口参数:  rec_ptr   ---        待存储的透传MAC结果项指针（输入）
**出口参数:  无
**函数功能:  追加一条透传MAC记录到MAC区RAM暂存缓冲；暂存满一整扇区则批量写入
**           FLASH并前移写指针，FLASH满环时覆盖最旧扇区
**返 回 值:  0表示成功，负值表示失败
*********************************************************************/
int my_flash_store_push_mac(const tran_mac_result_item_t *rec_ptr);

/********************************************************************
**函数名称:  my_flash_store_pending_count
**入口参数:  type      ---        数据类型（输入）
**出口参数:  无
**函数功能:  查询指定类型待上报记录总数(FLASH中有效记录 + RAM暂存未落盘记录)
**返 回 值:  待上报记录数量
*********************************************************************/
uint32_t my_flash_store_pending_count(fs_data_type_t type);

/********************************************************************
**函数名称:  my_flash_store_read_next
**入口参数:  type      ---        数据类型（输入）
**           out_rec_ptr ---      接收记录的缓冲区（输出，TAG传tag_scan_result_t*，
**                                MAC传tran_mac_result_item_t*）
**出口参数:  out_rec_ptr ---      存储读取到的一条记录
**函数功能:  读取下一条待上报记录(先读FLASH历史数据，再读RAM暂存)，只往后移动
**           读取位置，不删除数据、不写FLASH、不写ZMS
**返 回 值:  1表示读到一条，0表示已无数据，负值表示失败
*********************************************************************/
int my_flash_store_read_next(fs_data_type_t type, void *out_rec_ptr);

/********************************************************************
**函数名称:  my_flash_store_rewind
**入口参数:  type      ---        数据类型（输入）
**出口参数:  无
**函数功能:  把读取位置退回到上次commit(已确认)的地方，用于一批上报中途被
**           打断时复位，下次重新发送这批数据
**返 回 值:  无
*********************************************************************/
void my_flash_store_rewind(fs_data_type_t type);

/********************************************************************
**函数名称:  my_flash_store_upload_begin
**入口参数:  type      ---        数据类型（输入）
**出口参数:  无
**函数功能:  开启一次上报会话：立即记下当前暂存记录数并暂停落盘，此后push
**           不再写FLASH、读写指针保持不动，确保本轮实际发送数与START声明一致
**返 回 值:  无
**注意事项:  须在发送START、调用rewind之后、首条read_next之前调用；与commit
**           或rewind配对解除冻结
*********************************************************************/
void my_flash_store_upload_begin(fs_data_type_t type);

/********************************************************************
**函数名称:  my_flash_store_commit
**入口参数:  type      ---        数据类型（输入）
**出口参数:  无
**函数功能:  确认一整批已上报：把已确认的读取位置正式存到FLASH读指针上，并统一
**           写一次ZMS元数据。暂存里已发出确认的记录会被删除、剩余记录前移
**返 回 值:  0表示成功，负值表示失败
*********************************************************************/
int my_flash_store_commit(fs_data_type_t type);

/********************************************************************
**函数名称:  my_flash_store_clear
**入口参数:  type      ---        数据类型（输入）
**出口参数:  无
**函数功能:  清空指定类型存储区(读写指针归零、有效扇区清零、暂存清空)并持久化元数据
**返 回 值:  无
*********************************************************************/
void my_flash_store_clear(fs_data_type_t type);

#if FS_STORE_TEST_ENABLE
/********************************************************************
**函数名称:  my_flash_store_get_debug_info
**入口参数:  type      ---        数据类型（输入）
**           info_ptr  ---        接收调试状态的结构体指针（输出）
**出口参数:  info_ptr  ---        存储指定区的内部环形指针等运行状态
**函数功能:  导出指定类型存储区的内部运行状态，仅供测试或诊断查看，不改变任何状态
**返 回 值:  0表示成功，负值表示失败
**注意事项:  仅用于测试/调试，正常业务流程不依赖本接口
*********************************************************************/
int my_flash_store_get_debug_info(fs_data_type_t type, fs_debug_info_t *info_ptr);
#endif /* FS_STORE_TEST_ENABLE */

#endif /* _MY_FLASH_STORE_H_ */
